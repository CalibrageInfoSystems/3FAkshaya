package in.calibrage.akshaya.models;

public class uprootmentData {

    private String seedsPlanted,prevPalmsCount,plamsCount,isTreesMissing,missingTreesCount,reasonType,expectedPlamsCount,comments;
    public uprootmentData() {
        this.seedsPlanted = seedsPlanted;
        this.prevPalmsCount = prevPalmsCount;
        this.plamsCount = plamsCount;
        this.isTreesMissing = isTreesMissing;
        this.missingTreesCount = missingTreesCount;
        this.reasonType = reasonType;
        this.expectedPlamsCount = expectedPlamsCount;
        this.comments = comments;
    }
    public String getSeedsPlanted() {
        return seedsPlanted;
    }

    public void setSeedsPlanted(String seedsPlanted) {
        this.seedsPlanted = seedsPlanted;
    }

    public String getPrevPalmsCount() {
        return prevPalmsCount;
    }

    public void setPrevPalmsCount(String prevPalmsCount) {
        this.prevPalmsCount = prevPalmsCount;
    }

    public String getPlamsCount() {
        return plamsCount;
    }

    public void setPlamsCount(String plamsCount) {
        this.plamsCount = plamsCount;
    }

    public String getIsTreesMissing() {
        return isTreesMissing;
    }

    public void setIsTreesMissing(String isTreesMissing) {
        this.isTreesMissing = isTreesMissing;
    }

    public String getMissingTreesCount() {
        return missingTreesCount;
    }

    public void setMissingTreesCount(String missingTreesCount) {
        this.missingTreesCount = missingTreesCount;
    }

    public String getReasonType() {
        return reasonType;
    }

    public void setReasonType(String reasonType) {
        this.reasonType = reasonType;
    }

    public String getExpectedPlamsCount() {
        return expectedPlamsCount;
    }

    public void setExpectedPlamsCount(String expectedPlamsCount) {
        this.expectedPlamsCount = expectedPlamsCount;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

//
}
